void apps_print_hex_buffer (uint8_t* buffer ,uint16_t buffer_size )
{
	uint16_t i;
	for(i=0;i<buffer_size;i++)
	{
		if(i%16 == 0 )
		{
			printf(" \n\r ");
		}
		printf(" 0x%02X",buffer[i]);
	}
	printf("\n\r");
}

int main() {
	stse_ReturnCode_t stse_ret = STSE_API_INVALID_PARAMETER;
	stse_Handler_t stse_handler;
	uint8_t pRootCA[] = {ROOT_CA};

	uint8_t echo_tx_buffer[16] = {
		0x00,0x11,0x22,0x33,0x44,0x55,0x66,0x77,
		0x88,0x99,0xAA,0xBB,0xCC,0xDD,0xEE,0xFF};
	uint8_t echo_rx_buffer[16];

	/* ## Initialize STSE device handler */
	stse_ret = stse_set_default_handler_value(&stse_handler);
	stse_handler.device_type = STSAFE_A110;
	stse_ret = stse_init(&stse_handler);
	if (stse_ret != STSE_OK)
	{
		printf(PRINT_RED" ## stse_init ERROR : 0x%04X\n\r"PRINT_RESET,stse_ret);
		return 1;
	}
	printf(PRINT_CYAN"\n\r # Device init success\n\r"PRINT_RESET);

	/* ## Run simple echo command */
	printf(PRINT_CYAN"\n\r # Echo test\n\r"PRINT_RESET);
	stse_ret = stse_device_echo(
		&stse_handler,
		echo_tx_buffer,
		echo_rx_buffer,
		16);
	if (stse_ret != STSE_OK)
	{
		printf(PRINT_RED" ## stse_device_echo ERROR : 0x%04X\n\r"PRINT_RESET,stse_ret);
		return 1;
	}
	printf("    echo_tx_buffer:");
	apps_print_hex_buffer(echo_tx_buffer,16);
	printf("    echo_rx_buffer:");
	apps_print_hex_buffer(echo_rx_buffer,16);
	if (memcmp(echo_rx_buffer,echo_tx_buffer,16)!=0)
	{
		printf(PRINT_RED" ## stse_device_echo payload mismatch\n\r"PRINT_RESET);
		return 1;
	}

	/* ## Authenticate target STSE device */
	stse_ret = stse_device_authenticate(
		&stse_handler,
		pRootCA,
		0, 0);
	if (stse_ret != STSE_OK)
	{
		printf(PRINT_RED" ## stse_device_authenticate ERROR : 0x%04X\n\r"PRINT_RESET,stse_ret);
		return 1;
	}
	printf(PRINT_CYAN"\n\r # Device authentication success\n\r"PRINT_RESET);

	printf(PRINT_GREEN);
	printf("          _\r\n");
	printf("         /(|\r\n");
	printf("        (  :\r\n");
	printf("       __\\  \\  _____\r\n");
	printf("     (____)  `|\r\n");
	printf("    (____)|   |\r\n");
	printf("     (____).__|\r\n");
	printf("      (___)__.|_____\r\n\n\n");
	printf(PRINT_RESET);

	return 0;
}
